package clases;

public class Proveedores {
	
	private int prvCod, rubPrv;
	private String prvNom, prvTel, proDir, prvEstReg;
	
	public int getPrvCod() {
		return prvCod;
	}
	public void setPrvCod(int prvCod) {
		this.prvCod = prvCod;
	}
	public int getRubPrv() {
		return rubPrv;
	}
	public void setRubPrv(int rubPrv) {
		this.rubPrv = rubPrv;
	}
	public String getPrvNom() {
		return prvNom;
	}
	public void setPrvNom(String prvNom) {
		this.prvNom = prvNom;
	}
	public String getPrvTel() {
		return prvTel;
	}
	public void setPrvTel(String prvTel) {
		this.prvTel = prvTel;
	}
	public String getProDir() {
		return proDir;
	}
	public void setProDir(String proDir) {
		this.proDir = proDir;
	}
	public String getPrvEstReg() {
		return prvEstReg;
	}
	public void setPrvEstReg(String prvEstReg) {
		this.prvEstReg = prvEstReg;
	}
	
	

}
