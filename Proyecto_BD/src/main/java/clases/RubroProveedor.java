package clases;

public class RubroProveedor {
	String rubNom, rubEstReg;
	int rubProCod;
	
	public String getRubNom() {
		return rubNom;
	}
	public void setRubNom(String rubNom) {
		this.rubNom = rubNom;
	}
	public String getRubEstReg() {
		return rubEstReg;
	}
	public void setRubEstReg(String rubEstReg) {
		this.rubEstReg = rubEstReg;
	}
	public int getRubProCod() {
		return rubProCod;
	}
	public void setRubProCod(int rubProCod) {
		this.rubProCod = rubProCod;
	}
}
