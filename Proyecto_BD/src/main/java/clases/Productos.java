package clases;

public class Productos {
	private int proCod, proSto, UniMedCod, proCat;
	private String proNom, proEstReg;
	private double proPreVen, proPreCom;
	
	public int getProCod() {
		return proCod;
	}
	public void setProCod(int proCod) {
		this.proCod = proCod;
	}
	public int getProSto() {
		return proSto;
	}
	public void setProSto(int proSto) {
		this.proSto = proSto;
	}
	public int getUniMedCod() {
		return UniMedCod;
	}
	public void setUniMedCod(int uniMedCod) {
		UniMedCod = uniMedCod;
	}
	public int getProCat() {
		return proCat;
	}
	public void setProCat(int proCat) {
		this.proCat = proCat;
	}
	public String getProNom() {
		return proNom;
	}
	public void setProNom(String proNom) {
		this.proNom = proNom;
	}
	public String getProEstReg() {
		return proEstReg;
	}
	public void setProEstReg(String proEstReg) {
		this.proEstReg = proEstReg;
	}
	public double getProPreVen() {
		return proPreVen;
	}
	public void setProPreVen(double proPreVen) {
		this.proPreVen = proPreVen;
	}
	public double getProPreCom() {
		return proPreCom;
	}
	public void setProPreCom(double proPreCom) {
		this.proPreCom = proPreCom;
	}	

}
