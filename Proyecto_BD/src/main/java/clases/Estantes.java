package clases;

public class Estantes {
	
	private int estNum, almNum, estCap;
	private String estReg;
	public int getEstNum() {
		return estNum;
	}
	public void setEstNum(int estNum) {
		this.estNum = estNum;
	}
	public int getAlmNum() {
		return almNum;
	}
	public void setAlmNum(int almNum) {
		this.almNum = almNum;
	}
	public int getEstCap() {
		return estCap;
	}
	public void setEstCap(int estCap) {
		this.estCap = estCap;
	}
	public String getEstReg() {
		return estReg;
	}
	public void setEstReg(String estReg) {
		this.estReg = estReg;
	}
	
	
}
