package clases;

public class Almacenes {
	
	int almNum;
	String almDir, almEstReg;
	
	
	public int getAlmNum() {
		return almNum;
	}
	public void setAlmNum(int almNum) {
		this.almNum = almNum;
	}
	public String getAlmDir() {
		return almDir;
	}
	public void setAlmDir(String almDir) {
		this.almDir = almDir;
	}
	public String getAlmEstReg() {
		return almEstReg;
	}
	public void setAlmEstReg(String almEstReg) {
		this.almEstReg = almEstReg;
	}
	
	

}
