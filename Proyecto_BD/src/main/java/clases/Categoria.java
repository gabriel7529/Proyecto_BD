package clases;

public class Categoria {
	int catProCod;
	String Nombre;
	char estado;
	public int getCatProCod() {
		return catProCod;
	}
	public void setCatProCod(int catProCod) {
		this.catProCod = catProCod;
	}
	public String getNombre() {
		return Nombre;
	}
	public void setNombre(String nombre) {
		Nombre = nombre;
	}
	public char getEstado() {
		return estado;
	}
	public void setEstado(char estado) {
		this.estado = estado;
	}
}
